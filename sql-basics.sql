# List all Databases
>_ 	SHOW DATABASES

# Select and Use DATABASE
>_ 	USE <database_name>

# List all tables in the Databases
>_ 	SHOW TABLES

# Details description of table
>_ 	DESCRIBE <table_name> 	or
>_ 	DESC <table_name>

# List all Columns in the table
>_ 	SHOW COLUMNS FROM <table_name>

>_ 	SHOW <column_list, comma_separated, or * for all> FROM <table_name>

# Create a table
>_ CREATE TABLE <table_name>(<field_name, data_type , NOT NULL, DEFAULT>);

